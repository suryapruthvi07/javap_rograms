package b;

public class Bitwiseoperator {
	
	    public static void main(String[] args) {
	        // Define the numbers
	        int num1 = 73;
	        int num2 = 69;
	        int num3 = 89;

	        // Perform the bitwise AND operation
	        int result = num1 & num2 & num3;

	        // Display the result
	        System.out.println("The result of 73 & 69 & 89 is: " + result);
	    }
	}

	


